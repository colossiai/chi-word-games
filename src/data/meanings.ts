interface CharRow {
  c: string;
  py: string;
  r: string;
  k: number;
  s: number;
}
const CHAR_BY = new Map((charsData as CharRow[]).map((c) => [c.c, c]));

// 合併字義與官方字級資料，成為完整的 Character；查不到官方資料者略去。
const ALL: Character[] = ENTRIES.flatMap((e) => {
  const meta = CHAR_BY.get(e.c);
  if (!meta) return [];
  return [
    {
      char: e.c,
      pinyin: meta.py,
      meaning: e.m,
      radical: meta.r,
      strokes: meta.k,
      examples: e.ex,
      stage: meta.s as Stage,
    },
  ];
});

/** 依學習階段取「漢字精讀」卡。 */
export function charactersByStage(stage: Stage): Character[] {
  return ALL.filter((c) => c.stage === stage);
}
